    private void botonLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonLeerActionPerformed
        String fichero = ruta.getText();
        BufferedReader mibuffer = null;
        try {
            mibuffer = new BufferedReader(new FileReader(fichero));
            
            //3.leer datos
            for (int i = 0; i < fichero.length(); i++) {
                String linea = mibuffer.readLine();
                textArea.append(linea+"\n");
            }
            botonLeer.setEnabled(false);
            botonEscribir.setEnabled(true);
            
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "No se ha enncontrado el fichero, introduce una ruta correcta.","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, "Error leyendo.","ERROR",JOptionPane.ERROR_MESSAGE);
        }
        //4.cerrar flujos
        finally{
            try {
                if (mibuffer != null) mibuffer.close();// es como un if normal
            } catch (IOException e) {
                System.out.println("Error cerrando el flujo.");
            }
        }
    }//GEN-LAST:event_botonLeerActionPerformed

    private void botonEscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEscribirActionPerformed
        //2.creamos los flujos de lectura
        String fichero = destino.getText();
        BufferedWriter br = null;
        File archivo = new File(fichero);
        
        try {
            br = new BufferedWriter(new FileWriter(archivo,true));//el true lo que hace es activar el modo aÃ±adir, el modo append

            //3. escribir lo que necesite
            br.write(textArea.getText());
            
        } catch (IOException e) {
            System.out.println("Error escribiendo");
        }
        //4.cerrar flujos
        finally{
            
            try {
                if (br != null) br.close();//esto es una manera de poner el if cuando solo tiene una linea
            }
            catch (IOException e) {
                System.out.println("Fallo cerrando flujos");
            }
        }
    }//GEN-LAST:event_botonEscribirActionPerformed

