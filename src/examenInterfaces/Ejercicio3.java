    private void botonIntroducirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIntroducirActionPerformed
        String palabra = palabraNueva.getText();
        String letra = palabra.substring(0,1);
        System.out.println(letra);
        if ("a".equals(letra) || "e".equals(letra) || "i".equals(letra) || "o".equals(letra) || "u".equals(letra)) {
            listaVocales.addItem(palabra);
            palabraNueva.setText("");
        }
        else{
            listaConsonantes.addItem(palabra);
            palabraNueva.setText("");
        }
    }//GEN-LAST:event_botonIntroducirActionPerformed

    private void botonBorrarVocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarVocalActionPerformed
        if (listaVocales.getSelectedItem() == "") {
            JOptionPane.showMessageDialog(null, "No hay nada que borrar.");
        }else{
            listaVocales.removeItem(listaVocales.getSelectedItem());
        }
    }//GEN-LAST:event_botonBorrarVocalActionPerformed

    private void botonBorrarConsonanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarConsonanteActionPerformed
        if (listaConsonantes.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "No hay nada que borrar.");
        }else{
            listaConsonantes.removeItem(listaConsonantes.getSelectedItem());
        }
        
    }//GEN-LAST:event_botonBorrarConsonanteActionPerformed

